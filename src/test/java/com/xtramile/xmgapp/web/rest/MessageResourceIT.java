    @Test
    @Transactional
    void createMessage() throws Exception {
        int databaseSizeBeforeCreate = messageRepository.findAll().size();
        // Create the Message
        MessageDTO messageDTO = messageMapper.toDto(message);
        restMessageMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(messageDTO)))
            .andExpect(status().isCreated());

        // Validate the Message in the database
        List<Message> messageList = messageRepository.findAll();
        assertThat(messageList).hasSize(databaseSizeBeforeCreate + 1);
        Message testMessage = messageList.get(messageList.size() - 1);
        assertThat(testMessage.getOrderNo()).isEqualTo(DEFAULT_ORDER_NO);
        assertThat(testMessage.getValue()).isEqualTo(DEFAULT_VALUE);
        assertThat(testMessage.getCreatedBy()).isEqualTo(DEFAULT_CREATED_BY);
        assertThat(testMessage.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
        assertThat(testMessage.getLastModifiedBy()).isEqualTo(DEFAULT_LAST_MODIFIED_BY);
        assertThat(testMessage.getLastModifiedDate()).isEqualTo(DEFAULT_LAST_MODIFIED_DATE);
        assertThat(testMessage.getRecordStatusId()).isEqualTo(DEFAULT_RECORD_STATUS_ID);
    }

